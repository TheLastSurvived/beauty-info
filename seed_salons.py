def generate_phone():
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω—ã–π –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞"""
    return f"+7{random.randint(900, 999)}{random.randint(1000000, 9999999)}"

def generate_address(district):
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∞–¥—Ä–µ—Å –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ä–∞–π–æ–Ω–∞"""
    if district == "–õ–µ–Ω–∏–Ω—Å–∫–∏–π":
        street = random.choice(LENINSKY_STREETS)
    else:  # –û–∫—Ç—è–±—Ä—å—Å–∫–∏–π
        street = random.choice(OKTYABRSKY_STREETS)
    
    house = random.randint(1, 100)
    building = random.choice(["", "", "", f"–∫–æ—Ä–ø—É—Å {random.randint(1, 5)}"])
    apartment = random.choice(["", "", f"–æ—Ñ–∏—Å {random.randint(1, 50)}"])
    
    address = f"{street}, {house}"
    if building:
        address += f", {building}"
    if apartment:
        address += f", {apartment}"
    
    return address

